# 定义变量，记录钱包余额
money=130

print("你的钱包余额:", money, "元")
# 定义变量，记录商品价格
price=10

# 情景对话
print("拿一包滕王阁~")
print("需要支付:", price, "元")

# 重新赋值
money=money-price
print("消费后，你的钱包余额:", money, "元")

# 练习
# 钱包余额
money1=120
