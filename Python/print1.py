"""
错误了，为甚，｛错误写法："可口可乐"=5
               正确写法：可口可乐 = 5
               变量赋值时左侧不能是字符串
             （变量名不能加引号，等号左边必须是合法变量名）｝
"可口可乐"=5
print("再来一瓶可口可乐~", "需要支付:", "可口可乐", "元")
"""
